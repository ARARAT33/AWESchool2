    if (level === 4) { topicName = `${baseTopic}ի Ժամանակակից Մարտահրավերներ`; focus = 'Գլոբալ հետազոտություն և նորարարական լուծումներ'; }
    if (level === 5) { topicName = `${baseTopic} և Ապագայի Տեխնոլոգիաներ`; focus = 'Անկանխատեսելի փոխակերպումներ'; }

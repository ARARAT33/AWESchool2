/**
 * Builds the complete list of subjects dynamically.
 */
export const subjects: Subject[] = RAW_SUBJECT_DATA.map((s) => {
  return {
    id: s.id,
    name: s.name,
    description: s.desc,
    icon: s.icon,
    category: s.cat,
    lessons: generateLessonsForSubject(s.id, s.name, s.topics)
  };
});

export function getSubject(id: string): Subject | undefined {
  return subjects.find((s) => s.id === id);
}

export function getLesson(subjectId: string, lessonId: string): Lesson | undefined {
  const subject = getSubject(subjectId);
  return subject?.lessons.find((l) => l.id === lessonId);
}

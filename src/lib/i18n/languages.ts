// Լեզվի կոդի ավտոմատ որոշում — վերադարձնում է լեզվի կոդը կամ 'en' որպես ֆոլբեք
export function normalizeLangCode(code: string | undefined | null): string {
  if (!code) return 'hy'
  // Եթե կոդը կա մեր ցուցակում՝ օգտագործել այն
  if (LANGUAGES.find(l => l.code === code)) return code
  // Փորձել առաջին 2 տառերը
  const short = code.substring(0, 2)
  if (LANGUAGES.find(l => l.code === short)) return short
  return 'hy'
}
